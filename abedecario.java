public class abedecario {
    
private String letra;
private String letratransformada;

public abedecario(String letra, String letratransformada) {
    this.letra = letra;
    this.letratransformada = letratransformada;
}

public String getLetra() {
    return letra;
}

public String getLetratransformada() {
    return letratransformada;
}



}
